    private static Random random = new Random();

    public static void fetchObject(String url, JsonFetcherInterface call) {
        new Thread(() -> {
            try {
                URL url1 = new URL(url);
                StringBuilder response = new StringBuilder();

                InputStream stream = url1.openConnection().getInputStream();
                int i;
                while ((i = stream.read()) != -1) {
                    response.append((char) i);
                }
                System.out.println("response  : " + response.toString());
                call.fetched(new JSONObject(response.toString()));
                return;
            } catch (IOException | JSONException e) {
                e.printStackTrace();
            }
            call.fetched(null);
        }).start();
    }

    public static void fetchArray(String url, JsonFetcherInterface call) {
        new Thread(() -> {
            try {
                URL url1 = new URL(url);
                StringBuilder response = new StringBuilder();

                HttpURLConnection connection = (HttpURLConnection) url1.openConnection();
                connection.addRequestProperty("Host", "translate.googleapis.com");
                connection.addRequestProperty("User-Agent", userAgents[random.nextInt(userAgents.length)]);
                connection.addRequestProperty("Connection", "keep-alive");
                connection.addRequestProperty("Content-type", "application/json");
                connection.connect();
//                connection.addRequestProperty("User-Agent","Mozilla/5.0 (X11; Ubuntu; Linux x86_64; rv:72.0) Gecko/20100101 Firefox/72.0");
                InputStream stm = connection.getInputStream();
                InputStreamReader stream = new InputStreamReader(stm, StandardCharsets.UTF_8);
                int i;
                while ((i = stream.read()) != -1) {
                    response.append((char) i);
                }
                System.out.println("response  : " + response.toString());
                call.fetched(new JSONArray(response.toString()));
                return;
            } catch (IOException | JSONException e) {
                call.fetched("error");
                e.printStackTrace();
            }
            call.fetched(null);
        }).start();
    }
